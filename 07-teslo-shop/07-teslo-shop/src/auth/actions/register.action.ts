import { tesloApi } from "@/api/tesloApi"
import type { AuthResponse } from "../interfaces/auth.response";

export const RegisterAction = async (
    email: string,
    password: string,
    fullName: string,
): Promise<AuthResponse> => {

    try {
        const { data } = await tesloApi.post('/auth/register', {
            email,
            password,
            fullName
        });
        console.log({ data });
        
        return data;
    } catch (error) {
        console.log({error});
        throw error;
    }    
}